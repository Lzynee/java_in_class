package com.mystudy.week2.run;

public class VariableInitializationExample {
    public static void main(String[] args) {
        //변수 value  선언
        int value = 0;

        //연산 결과를 변수 result 초기 값으로 대입
        int result = value + 10;

        //변수 result 값을 읽고 콘솔을 출력
        System.out.println(result);
    }
}
